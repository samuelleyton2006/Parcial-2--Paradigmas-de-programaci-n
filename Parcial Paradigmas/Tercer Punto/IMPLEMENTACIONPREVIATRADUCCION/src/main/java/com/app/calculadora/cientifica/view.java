    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        if (!txtOperaciones.getText().isEmpty()) {
            // Crear un ScriptEngine para evaluar expresiones JavaScript
            ScriptEngine engine = new ScriptEngineManager().getEngineByName("JavaScript");
            String operacion = txtOperaciones.getText();
            try {
                Object result = engine.eval(operacion);
                txtResultado.setText(result.toString());
            } catch (ScriptException ex) {
                System.out.println(ex.getMessage());
                JOptionPane.showMessageDialog(null,
                        "Ocurrio un error al realizar la operacion, valide la operacion suministrada", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnIgualActionPerformed

